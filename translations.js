// Language direction map
const directionMap = {
  fa: 'rtl',
  tr: 'ltr'
};

const languageOrder = ['fa', 'tr', 'en'];
// Get current language from localStorage or default to Persian
let currentLanguage = localStorage.getItem('siteLang') || 'fa';

// Translation function
function getTranslation(key) {
  return translations[currentLanguage]?.[key] || translations.fa[key] || key;
}

// Apply translations to all elements
function applyTranslations() {
  // Update HTML attributes
  document.documentElement.lang = currentLanguage;
  document.documentElement.dir = directionMap[currentLanguage] || 'rtl';
  
  // Translate all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = getTranslation(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = translation;
    } else {
      el.textContent = translation;
    }
  });
  
  // Translate elements with data-i18n-lang (language switcher buttons)
  document.querySelectorAll('[data-i18n-lang]').forEach(el => {
    const lang = el.getAttribute('data-i18n-lang');
    if (lang === 'fa') {
      el.textContent = 'ÙØ§Ø±Ø³ÛŒ';
    } else if (lang === 'tr') {
      el.textContent = 'TÃ¼rkÃ§e';
    }
  });
  
  // Update active language button styles
  document.querySelectorAll('.lang-switch-btn').forEach(btn => {
    const btnLang = btn.getAttribute('data-lang');
    if (btnLang === currentLanguage) {
      btn.classList.add('bg-white/30', 'text-white');
      btn.classList.remove('text-white/70');
    } else {
      btn.classList.remove('bg-white/30');
      btn.classList.add('text-white/70');
    }
  });
  
  // Save to localStorage
  localStorage.setItem('siteLang', currentLanguage);
  
  // Update chatbot if it exists
  if (typeof updateChatbotLanguage === 'function') {
    updateChatbotLanguage();
  }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  
  // Add event listeners to language switcher buttons
  document.querySelectorAll('.lang-switch-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const newLang = btn.getAttribute('data-lang');
      if (newLang && newLang !== currentLanguage) {
        currentLanguage = newLang;
        applyTranslations();
      }
    });
  });
  
  // Mobile menu toggle
  const mobileMenuBtn = document.getElementById('mobile-menu-btn');
  const mobileMenu = document.getElementById('mobile-menu');
  
  if (mobileMenuBtn && mobileMenu) {
    mobileMenuBtn.addEventListener('click', () => {
      mobileMenu.classList.toggle('hidden');
      const icon = mobileMenuBtn.querySelector('i');
      if (icon) {
        icon.classList.toggle('fa-bars');
        icon.classList.toggle('fa-times');
      }
    });
  }
});

// Export for use in other scripts
window.getTranslation = getTranslation;
window.currentLanguage = () => currentLanguage;

